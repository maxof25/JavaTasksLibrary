public class ThreeLittleNumbers {
	public static void main(String[] args) {
		int n1 = 25;
		//I first declare the variable of which will be holding a value that is an integer.
		//I then place the value i wish to store in the variable in n1 (my first number). The value is 25.
		int n2 = 54;
		//I then declare the variable of which will be holding a value that is also an integer (a whole number).
		//In the variable n2 (my second number), the int value '54' is held.
		int n3 = 78;
		//I then declare the variable of which will be holding a value that is an integer (a whole number).
		//In the variable n3 (my third number), the int value '78' is held.
		System.out.println("Number 1 is " + n1);
		//This line will print out "Number 1 is " then the stored value in the variable n1 in the terminal of the IDE.
		System.out.println("Number 2 is " + n2);
		//This line will print out "Number 2 is " then the stored value in the variable n2 in the terminal of the IDE.
		System.out.println("Number 3 is " + n3);
		//This line will print out "Number 3 is " then the stored value in the variable n3 in the terminal of the IDE.
		System.out.println("The sum is " + (n1 + n2 + n3));
		//This line will print out "The sum is " followed by the sum of all the values of each variable in the second pair of brackets in the terminal of the IDE.
		//(n1 + n2 + n3) will add all three numbers stored in each of the variables together.
		System.out.println("The product is " + (n1 * n2 * n3));
		//This line will print out "The product is " followed by the  product of all the values of each variable in the second pair of brackets in the terminal of the IDE.
		//(n1 * n2 * n3) will multiply all three numbers stored in each of the variables together.
	}
}
